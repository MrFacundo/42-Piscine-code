#include <stdio.h>

int	ft_iterative_factorial(int nb)
{
	int	result;

	if (nb < 0)
		return (0);
	result = nb;
	while (nb > 2)
	{
		result *= nb - 1;
		nb--;
	}
	return (result);
}

