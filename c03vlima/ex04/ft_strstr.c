char	*ft_strstr(char *str, char *to_find)
{
	int	i;
	int	i1;

	i = 0;
	if (*to_find == '\0')
	{
		return (str);
	}
	while (str[i] != '\0')
	{
		i1 = 0;
		while (str[i + i1] != '\0' && str[i + i1] == to_find[i1])
		{
			if (to_find[i1 + 1] == '\0')
			{
				return (&str[i]);
			}
			i1++;
		}
		i++;
	}
	return (0);
}
